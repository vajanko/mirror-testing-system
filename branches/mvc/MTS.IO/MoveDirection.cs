namespace MTS.IO
{
    /// <summary>
    /// Direction of mirror movement
    /// </summary>
    public enum MoveDirection
    {
        /// <summary>
        /// Move mirror up (north)
        /// </summary>
        Up,
        /// <summary>
        /// Move mirror down (south)
        /// </summary>
        Down,
        /// <summary>
        /// Move mirror left (west)
        /// </summary>
        Left,
        /// <summary>
        /// Move mirror right (east)
        /// </summary>
        Right,
        /// <summary>
        /// Do not move mirror
        /// </summary>
        None
    }
}
